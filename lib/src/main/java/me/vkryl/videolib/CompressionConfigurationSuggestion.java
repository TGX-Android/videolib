package me.vkryl.videolib;

import android.content.Context;
import android.net.Uri;

/**
 * Configuration suggestion generated by the library.
 *
 * On the client side, this will be used when displaying a slider.
 */
public class CompressionConfigurationSuggestion {
  /**
   * Unique name of the suggestion within a suggestion list.
   *
   * Can be the same as {@link #displayName} (but not limited to its value).
   * Example: "1080p60fps", "720p", "360p", etc
   */
  public String id;

  /**
   * Identifier of the suggestion group.
   *
   * Suggestions with the same <code>groupId</code> will be combined on the UI level.
   *
   * Example: if library uses "1080p60fp" and "1080p" in the {@link #id},
   * <code>groupId</code> could be equal to "1080p" for both of the suggestions.
   *
   * Note that these example values are just a suggestion,
   * and you are free to use whatever values you find more reasonable.
   */
  public String groupId;

  /**
   * Display name of the suggestion that could be shown to the end user.
   *
   * Example: "1080p60fps", "720p", etc.
   */
  public String displayName;

  /**
   * Estimated output size for the suggested configuration.
   */
  public EstimatedOutputSize estimatedSize;

  /**
   * Suggested configuration to pass to the {@link VideoLibrary#convertVideo(Context, Uri, OutputVideoConfiguration, OverlayProvider, String, ConversionCallback)}
   */
  public CompressionConfiguration configuration;
}
